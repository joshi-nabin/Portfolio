// Add intersection observer for counting animations
function initCountingAnimations() {
    const statNumbers = document.querySelectorAll('.stat-number');
    
    const countObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const target = entry.target;
                const finalText = target.textContent;
                
                // Only animate if it contains numbers
                if (/\d/.test(finalText)) {
                    const finalNumber = parseInt(finalText.match(/\d+/)[0]);
                    let currentNumber = 0;
                    const increment = finalNumber / 30;
                    
                    const countTimer = setInterval(() => {
                        currentNumber += increment;
                        if (currentNumber >= finalNumber) {
                            target.textContent = finalText;
                            clearInterval(countTimer);
                        } else {
                            target.textContent = finalText.replace(/\d+/, Math.floor(currentNumber));
                        }
                    }, 50);
                }
                
                countObserver.unobserve(target);
            }
        });
    }, {
        threshold: 0.5
    });
    
    statNumbers.forEach(stat => {
        countObserver.observe(stat);
    });
}

// Initialize counting animations
initCountingAnimations();

// Add theme toggle functionality (bonus feature)
function initThemeToggle() {
    const themeToggle = document.createElement('button');
    themeToggle.className = 'theme-toggle';
    themeToggle.innerHTML = '<i class="fas fa-moon"></i>';
    themeToggle.style.cssText = `
        position: fixed;
        top: 50%;
        left: 20px;
        transform: translateY(-50%);
        width: 50px;
        height: 50px;
        background: rgba(255, 255, 255, 0.1);
        backdrop-filter: blur(10px);
        border: 1px solid rgba(255, 255, 255, 0.2);
        border-radius: 50%;
        color: white;
        cursor: pointer;
        font-size: 1.2rem;
        transition: all 0.3s ease;
        z-index: 1000;
    `;
    
    document.body.appendChild(themeToggle);
    
    themeToggle.addEventListener('click', function() {
        document.body.classList.toggle('dark-mode');
        const icon = this.querySelector('i');
        
        if (document.body.classList.contains('dark-mode')) {
            icon.className = 'fas fa-sun';
            this.style.background = 'rgba(0, 0, 0, 0.2)';
        } else {
            icon.className = 'fas fa-moon';
            this.style.background = 'rgba(255, 255, 255, 0.1)';
        }
    });
    
    themeToggle.addEventListener('mouseenter', function() {
        this.style.transform = 'translateY(-50%) scale(1.1)';
    });
    
    themeToggle.addEventListener('mouseleave', function() {
        this.style.transform = 'translateY(-50%) scale(1)';
    });
}

// Initialize theme toggle
initThemeToggle();

